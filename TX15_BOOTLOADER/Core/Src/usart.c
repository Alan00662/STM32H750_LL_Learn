void uart5_send_byte(uint8_t Data)
{
	HAL_UART_Transmit(&huart5, (uint8_t *)&Data,1, 100);

}

void uart5_send_buf(uint8_t *buf, uint8_t len)
{
    uint8_t i;
    for (i = 0; i < len; i++)
    {
        uart5_send_byte(*(buf + i));
    }
}

void uart5_send_string(char *str)
{
    uint8_t i;
    for (i = 0; i < strlen(str); i++)
    {
        uart5_send_byte(*(str + i));
    }
}

